package june27;

public class LogicalOperator {
    public static void main(String[] args) {

        int x = 10;
        int y = 20;
        int z = 30;
        System.out.println(x<y && x>z); // && is logical AND operator
        /*
        AND :
        TRUE TRUE --> TRUE
         */

        System.out.println(x<y || x > z); // && is logical OR operator
        // ! logical NOT operator !x = 10;
    }
}
