public class Division {
    public static void main(String[] args){
        int a = 15;
        int b = 8;
        int result = a / b;

        System.out.println("Result = " +result);

        double x = 15.00;
        double y = 8.00;
        double output = x / y;

        System.out.println("Result = " +output);
    }
}
