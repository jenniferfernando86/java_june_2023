package july15;

public class Animal {
    public void hunt(String animal, String food) {
        System.out.println("The food for the "+animal+" is '" + food +"'.");
    }
    public void sleep(String animal, int hours) {
        System.out.println("The " + animal + " can sleep for '" + hours + "' hours.");
    }
    public void sound(String animal, String sound) {
        System.out.println("The " + animal + " can make this sound '" + sound+"'.\n");
    }

}
