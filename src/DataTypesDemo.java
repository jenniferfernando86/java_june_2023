public class DataTypesDemo {
    public static void main(String[] args){
        // ++ increment ? +1
        // -- increment ? -1
        // post increment x++;
        // pre increment  ++x
        int x = 10;
        x++;
        System.out.println("X : " +x);

        int y = 10;
        ++y;
        System.out.println("y : " +y);
    }
}
// assignment operator ---> x = 10; y = 23; --> it operates from right to left
